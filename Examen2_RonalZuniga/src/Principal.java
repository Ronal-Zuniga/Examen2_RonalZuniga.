    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        adminCliente ac = new adminCliente("./Clientes.rjz");
        if (!nc.getText().isEmpty() && !apc.getText().isEmpty() && !idc.getText().isEmpty()) {
            ac.cargarArchivo();
            String nombre = nc.getText();
            String apellido = apc.getText();
            int id = Integer.parseInt(idc.getText());
            Cliente c = new Cliente(nombre, apellido, id);
            ac.setCliente(c);
            ac.escribirArchivo();
            JOptionPane.showMessageDialog(this, "Cliente agregado correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "Campos incompletos, Cliente NO fue agregado");
        }
    }//GEN-LAST:event_jButton1MouseClicked

